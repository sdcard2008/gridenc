from gridenc import gridenclib 


def test_grid_init():
    assert gridenclib.init_grid(5,1234) == [
        ['\x0c', '%', 'o', 'a', 'm'], ['_', 'f', '7', '\t', 'n'],
        ['r', 'Z', 'K', 'd', 'g'],
        ['e', '$', '\x0b', ' ', '['],
        ['\n', '?', 'C', 'u', 'I'],
        ['y', 'c', '1', '~', 'U'],
        ['s', '}', 't', 'z', '\r'],
        ['l', '&', '`', '(', 'k'],
        [':', 'N', '"', '6', 'R'],
        ['L', 'H', '\\', 'E', '*'],
        ['/', '0', 'h', ';', 'q'],
        ['b', 'Y', 'F', '>', '+'],
        ['J', 'v', 'x', '2', 'P'],
        ['A', 'W', 'Q', 'j', '8'],
        ['X', '|', 'B', '.', 'G'],
        ['3', '!', '<', 'p', '='],
        ['#', 'w', 'T', ',', 'S'],
        ['V', '9', 'M', ']', 'i'],
        ['5', '^', '@', ')', "'"],
        ['4', '{', 'O', 'D', '-']]
    assert gridenclib.init_grid(10,2008) == [
    ['s', '=', 'Y', 'o', 'g', '#', '"', 'Z', 'w', 'U'],
    [':', 'H', 'G', '}', '%', '3', '1', 'z', 'P', 'x'],
    ['>', 'A', ' ', '8', '7', 'D', '?', 'S', '[', 'T'],
    ['i', 'V', 'c', '!', ']', 'b', 'y', 'K', '~', 'k'],
    ['W', ',', '{', 'm', 'e', '(', 'Q', ';', '\x0c', 'J'],
    ['d', '6', 'r', 'q', 'X', '+', 'h', '<', 'l', 'p'],
    ['N', "'", 'a', '*', 'f', '.', '9', 'u', '`', 'R'],
    ['E', '/', '_', '\x0b', '\n', 'I', 'n', '$', 'C', 'j'],
    ['\\', 'B', '2', 't', 'F', 'L', '4', 'M', '|', '-'],
    ['^', 'v', '@', '&', '0', '5', '\t', 'O', '\r', ')']]